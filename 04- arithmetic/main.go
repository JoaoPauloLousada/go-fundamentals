package main

import "fmt"

func main() {
	a, b := 10, 5
	c := a + b // addition
	fmt.Println(c)
	c = a - b // subtraction
	fmt.Println(c)
	c = a * b //multiplication
	fmt.Println(c)
	c = a / b // division
	fmt.Println(c)
	c = a % 3 // modulus
	fmt.Println(c)
	d := 7.0 / 2.0 // decimal results
	fmt.Println(d)
}
